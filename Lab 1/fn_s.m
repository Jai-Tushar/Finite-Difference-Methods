function [dydt] = fn_s(t,y)

    global b
   
    dy(1) = y(1)^2*((2*b*t)/((1/(t^2))-b))+ y(1)*((4*b*(1/t)-2*t)/(((1/t^(2)))-b));
            
    
    dy(2) = y(2)*((4*b*((1/t^(3)))-4*(1/t))/(1-b*((1/t^(2))))) + ...
        y(1)*((4*b*((1/t^(5))))/(1+b*((1/t^(2))))) + ...
        y(1)*y(2)*((-4*b*(1/(1+t)))/(1-b*((1/t^(2)))));
    
    dydt = [dy(1);dy(2)];

end